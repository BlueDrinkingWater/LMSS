import express from 'express';
import { addPlayer, updatePlayerStats, getTeamPlayers } from '../controllers/playerController.js';
import { protect } from '../middleware/authMiddleware.js';
import upload from '../middleware/uploadMiddleware.js';

const router = express.Router();

router.post('/', protect, upload.single('photo'), addPlayer);
router.put('/:playerId/stats', protect, updatePlayerStats);
router.get('/team/:teamId', protect, getTeamPlayers);

export default router;